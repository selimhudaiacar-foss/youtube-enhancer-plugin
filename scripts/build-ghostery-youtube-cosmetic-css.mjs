import fs from "node:fs/promises";
import path from "node:path";

const PREFIX = "https://raw.githubusercontent.com/ghostery/adblocker/master/packages/adblocker/assets";
const SOURCES = [
  `${PREFIX}/easylist/easylist.txt`,
  `${PREFIX}/ublock-origin/filters.txt`,
  `${PREFIX}/ublock-origin/quick-fixes.txt`
];

const OUTPUT_FILE = path.resolve(process.cwd(), "rules/ghostery-youtube-cosmetic.css");
const SELECTOR_LIMIT = 1200;

const YOUTUBE_DOMAIN_RE = /(^|\.)youtube\.com$|(^|\.)youtube-nocookie\.com$|(^|\.)youtubekids\.com$|(^|\.)m\.youtube\.com$|(^|\.)music\.youtube\.com$|(^|\.)tv\.youtube\.com$/i;
const UNSUPPORTED_SELECTOR_RE = /(\+js\(|:has-text\(|:xpath\(|:upward\(|:remove(?:-attr|-class)?\(|:matches-css(?:-before|-after)?\(|:style\(|:-abp-|:nth-ancestor\(|:watch-attr\(|:if\(|:if-not\(|:contains\()/i;

function normalizeDomain(domain) {
  return domain.trim().toLowerCase().replace(/^\~/, "").replace(/^\*\./, "").replace(/\.$/, "");
}

function domainTargetsYoutube(domainPart) {
  const rawDomains = domainPart.split(",").map((d) => d.trim()).filter(Boolean);
  if (rawDomains.length === 0) return false;

  const positiveDomains = rawDomains.filter((domain) => !domain.startsWith("~"));
  if (positiveDomains.length === 0) return false;

  return positiveDomains.some((domain) => YOUTUBE_DOMAIN_RE.test(normalizeDomain(domain)));
}

function parseSelectorLine(line) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("!") || trimmed.startsWith("#")) return null;
  if (trimmed.includes("#@#")) return null;

  const markerIndex = trimmed.indexOf("##");
  if (markerIndex < 0) return null;

  const domainPart = trimmed.slice(0, markerIndex).trim();
  const selector = trimmed.slice(markerIndex + 2).trim();
  if (!domainPart || !selector) return null;
  if (!domainTargetsYoutube(domainPart)) return null;
  if (selector.startsWith("^")) return null;
  if (UNSUPPORTED_SELECTOR_RE.test(selector)) return null;
  if (selector.includes("\n") || selector.includes("\r")) return null;
  return selector;
}

async function fetchText(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status} ${response.statusText}`);
  }
  return response.text();
}

async function buildSelectors() {
  const selectors = new Set();

  for (const source of SOURCES) {
    const text = await fetchText(source);
    for (const line of text.split(/\r?\n/)) {
      const selector = parseSelectorLine(line);
      if (!selector) continue;
      selectors.add(selector);
      if (selectors.size >= SELECTOR_LIMIT) break;
    }
    if (selectors.size >= SELECTOR_LIMIT) break;
  }

  return [...selectors].sort((a, b) => a.localeCompare(b));
}

function buildCss(selectors) {
  const header = [
    "/*",
    " * Ghostery-derived YouTube cosmetic filters.",
    " * Generated by scripts/build-ghostery-youtube-cosmetic-css.mjs",
    " */"
  ].join("\n");

  const body = selectors
    .map((selector) => `${selector}{display:none !important;visibility:hidden !important;opacity:0 !important;pointer-events:none !important;}`)
    .join("\n");

  return `${header}\n${body}\n`;
}

async function main() {
  const selectors = await buildSelectors();
  const css = buildCss(selectors);
  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, css, "utf8");
  console.log(`Generated ${selectors.length} Ghostery YouTube cosmetic selectors -> ${OUTPUT_FILE}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
